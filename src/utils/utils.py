def convert_to_absolute(x, y, w, h, i, j, image_width, image_height, S):
    cell_width = image_width / S
    cell_height = image_height / S

    w = w * image_width
    h = h * image_height
    x = j * cell_width + x * cell_width
    y = i * cell_height + y * cell_height
    return x, y, w, h


